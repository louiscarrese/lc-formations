            <li>Un justificatif de situation (pour les demandeurs d'emploi qui souhaitent bénéficier d'un tarif préférentiel)</li>
            <li>Un exemplaire de la convention de formation signée (pour les salariés dont les employeurs financent la formation)</li>
        </ul>
    </p>
