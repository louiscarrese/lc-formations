    <h1>Modalités et conditions d’inscription</h1>
</div>
<div class="modal-body">
    <p>
        La souscription de la fiche d’inscription à une formation entraîne l’acceptation par le participant des modalités et conditions décrites ci-dessous.
    </p>

    <h2>Fiche d’inscription</h2>
    <p>
        Une fiche par personne et par formation, à remplir obligatoirement. 
    </p>

    <h2>Lieu</h2>
    <p>
        Sauf mention contraire, toutes nos formations ont lieu dans nos locaux. Le Jardin Moderne propose également des formations en "intra", organisées sur mesure pour le public d'une structure.
    </p>

    <h2>Tarifs</h2>
    <p>
        Nos tarifs sont nets. Les frais de repas et d’hébergement (si besoin) ne sont pas compris dans le montant de la formation. L’adhésion à l’association Le Jardin Moderne est obligatoire.
    </p>

    <h2>Effectif</h2>
    <p>
        Le nombre de participants est volontairement limité afin de former un groupe favorisant les interactions riches d’enseignements. Les inscriptions sont enregistrées dans leur ordre d’arrivée. Quatre jours avant la date du début de la formation, le Jardin Moderne confirmera par mail les dates et horaires.
    </p>

    <h2>Règlement</h2>
    <p>
        La fiche d’inscription doit impérativement être accompagnée du règlement (chèque à l’ordre du Jardin Moderne) ou de l’attestation de prise en charge par l’employeur ou de l’OPCA dont dépend le participant.
    </p>
    <p>
        Les chèques ne seront encaissés qu’après la formation.
    </p>
    <p>
        Pour certaines formations, il est possible, après validation par notre secteur formation, de régler en plusieurs fois.
    </p>

    <h2>Financements possibles</h2>
    <p>
        Le Jardin Moderne étant un organisme de formations enregistré à la DIRRECTE, différentes prises en charge peuvent être envisagées selon votre situation personnelle.
    </p>
    <p>
        Nos formations peuvent être financées par votre employeur par l’AFDAS, l’Agefos PME ou d’autres Fonds d’assurance formation.
    </p>
    <p>
        D’autres prises en charge partielles ou totales peuvent être étudiées. Nous nous tenons à votre disposition pour vous informer sur les possibilités d’aides dont vous pourriez bénéficier.
    </p>

    <h2>Modifications de la formation</h2> 
    <p>
        Si le nombre d’inscrits s’avère insuffisant, le Jardin Moderne se réserve la possibilité de reporter ou d’annuler la session de formation. Les participants seraient dans ce cas tenus informés minimum quatre jours avant le début de la formation. En cas d’annulation, le règlement correspondant à l’inscription leur sera entièrement restitué.
    </p>
    <p>
        Le Jardin Moderne se réserve le droit de faire intervenir un autre formateur que celui annoncé dans le programme, en cas d’empêchement de la part de l’intervenant initialement prévu.
    </p>

    <h2>Annulation par le stagiaire</h2> 
    <p>
        Le stagiaire est tenu de prévenir en cas d'absence.
    </p>
    <p>
        Dans le cas d'une prise en charge (par l'OPCA ou l'employeur), la période d'absence, sans justificatif, sera facturée au stagiaire.
    </p>
    <p>
        Si l'annulation est signifiée par écrit, au moins 15 jours calendaires avant le début de la formation, elle donnera lieu à un remboursement. En deçà, le montant du règlement de la formation reste dû.
    </p>
